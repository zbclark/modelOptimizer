function getDeltaPlayerScoresForEvent(eventId, season) {
  const key = eventId !== null && eventId !== undefined ? String(eventId).trim() : '';
  const entry = DELTA_PLAYER_SCORES[key];
  if (!entry) return {};
  if (season !== null && season !== undefined) {
    const seasonValue = parseInt(String(season).trim(), 10);
    if (!Number.isNaN(seasonValue) && entry.season && entry.season !== seasonValue) {
      return {};
    }
  }
  return entry.players || {};
}

